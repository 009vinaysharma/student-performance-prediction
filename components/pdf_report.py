"""
components/pdf_report.py
Generates a downloadable PDF report for a single prediction using fpdf2.
"""

from fpdf import FPDF
from datetime import datetime


class PredictionPDF(FPDF):
    def header(self):
        self.set_fill_color(99, 102, 241)
        self.rect(0, 0, 210, 25, "F")
        self.set_text_color(255, 255, 255)
        self.set_font("Helvetica", "B", 16)
        self.set_xy(10, 8)
        self.cell(0, 10, "Student Performance Prediction Report", ln=True)
        self.set_font("Helvetica", "", 9)
        self.set_xy(10, 17)
        self.cell(0, 6, f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')}", ln=True)
        self.ln(15)

    def footer(self):
        self.set_y(-15)
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(140, 140, 140)
        self.cell(0, 10, "Generated by AI Student Performance Dashboard", align="C")


def generate_pdf_report(form_data: dict, result: dict, recommendations: list) -> bytes:
    pdf = PredictionPDF()
    pdf.add_page()
    pdf.set_text_color(20, 20, 20)

    pdf.set_font("Helvetica", "B", 13)
    pdf.cell(0, 10, "Student Profile", ln=True)
    pdf.set_font("Helvetica", "", 11)

    labels = {
        "age": "Age",
        "gender": "Gender",
        "study_hours_per_week": "Study Hours / Week",
        "attendance_percentage": "Attendance %",
        "previous_grade": "Previous Grade",
        "sleep_hours": "Sleep Hours / Night",
        "parental_support": "Parental Support",
        "extracurricular_activities": "Extracurricular Activities",
        "internet_access": "Internet Access",
        "part_time_job": "Part-Time Job",
    }
    for key, label in labels.items():
        pdf.cell(0, 7, f"{label}: {form_data.get(key)}", ln=True)

    pdf.ln(5)
    pdf.set_font("Helvetica", "B", 13)
    pdf.cell(0, 10, "Prediction Result", ln=True)
    pdf.set_font("Helvetica", "", 11)

    grade = result.get("predicted_grade")
    status = result.get("status")
    confidence = result.get("confidence")

    pdf.cell(0, 7, f"Predicted Final Grade: {grade:.1f} / 100" if grade is not None else "N/A", ln=True)
    pdf.cell(0, 7, f"Predicted Status: {status}", ln=True)
    if confidence is not None:
        pdf.cell(0, 7, f"Confidence Score: {confidence * 100:.1f}%", ln=True)

    probs = result.get("probabilities", {})
    if probs:
        pdf.ln(2)
        pdf.set_font("Helvetica", "B", 11)
        pdf.cell(0, 7, "Class Probabilities:", ln=True)
        pdf.set_font("Helvetica", "", 11)
        for cls, p in probs.items():
            pdf.cell(0, 7, f"  {cls}: {p * 100:.1f}%", ln=True)

    pdf.ln(5)
    pdf.set_font("Helvetica", "B", 13)
    pdf.cell(0, 10, "AI Recommendations", ln=True)
    pdf.set_font("Helvetica", "", 11)
    for tip in recommendations:
        clean_tip = "".join(ch for ch in tip if 32 <= ord(ch) < 256).strip()
        if not clean_tip:
            continue
        pdf.set_x(10)
        pdf.multi_cell(190, 7, f"- {clean_tip}")

    return bytes(pdf.output(dest="S"))
